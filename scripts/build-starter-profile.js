#!/usr/bin/env node

const fs = require('fs')
const path = require('path')

const repoRoot = process.cwd()
const profileDir = path.join(repoRoot, 'starter-profile')
const filesManifest = path.join(repoRoot, 'starter', 'starter-files.txt')

function ensureDir (dirPath) {
  fs.mkdirSync(dirPath, { recursive: true })
}

function copyFileWithParents (sourcePath, destinationRoot) {
  const relative = path.relative(repoRoot, sourcePath)
  const destination = path.join(destinationRoot, relative)
  ensureDir(path.dirname(destination))
  fs.copyFileSync(sourcePath, destination)
}

function copyDirectoryRecursive (sourceDir, destinationRoot) {
  const entries = fs.readdirSync(sourceDir, { withFileTypes: true })

  entries.forEach(entry => {
    const fullPath = path.join(sourceDir, entry.name)

    if (entry.isDirectory()) {
      copyDirectoryRecursive(fullPath, destinationRoot)
      return
    }

    if (entry.isFile()) copyFileWithParents(fullPath, destinationRoot)
  })
}

if (!fs.existsSync(filesManifest)) {
  console.error('Missing starter manifest: starter/starter-files.txt')
  process.exit(1)
}

if (fs.existsSync(profileDir)) {
  fs.rmSync(profileDir, { recursive: true, force: true })
}

ensureDir(profileDir)

const patterns = fs.readFileSync(filesManifest, 'utf8')
  .split('\n')
  .map(line => line.trim())
  .filter(line => line && !line.startsWith('#'))

patterns.forEach(pattern => {
  if (pattern.endsWith('/**')) {
    const base = pattern.slice(0, -3)
    const sourceDir = path.join(repoRoot, base)

    if (!fs.existsSync(sourceDir)) {
      console.error(`Missing directory for pattern: ${pattern}`)
      process.exit(1)
    }

    copyDirectoryRecursive(sourceDir, profileDir)
    return
  }

  const sourcePath = path.join(repoRoot, pattern)
  if (!fs.existsSync(sourcePath)) {
    console.error(`Missing file: ${pattern}`)
    process.exit(1)
  }

  const stat = fs.statSync(sourcePath)
  if (stat.isDirectory()) {
    copyDirectoryRecursive(sourcePath, profileDir)
  } else {
    copyFileWithParents(sourcePath, profileDir)
  }
})

const readmePath = path.join(profileDir, 'STARTER-PROFILE.md')
fs.writeFileSync(readmePath, [
  '# Starter Profile Output',
  '',
  'This folder is generated by `npm run starter:build-profile`.',
  'Use it as a base when creating a new theme variant without cloning all advanced templates.',
  '',
  'Generated from manifest: `starter/starter-files.txt`.'
].join('\n'))

console.log('Starter profile generated at: starter-profile/')
